#!/bin/bash

echo "🔄 Ініціалізація фрактального життєвого циклу…"

# Основна директорія життєвого циклу
BASE_DIR="🧬/💓серцебиття/фази"
mkdir -p "$BASE_DIR"

# Список фаз у лапках
phases="🕳️_зародження 🌱_паросток 💨_дихання 💓_серцебиття 🧠_усвідомлення 🌀_мутація 🚪_розкриття ⚖️_баланс 💀_розкладення ♻️_регенерація"

# Створюємо папки для кожної фази
for phase in $phases; do
  mkdir -p "$BASE_DIR/$phase"
done

# README
cat > "🧬/💓серцебиття/README.md" <<EOL
# 💓 Серцебиття

Фази життєвого циклу фрактального обʼєкта:

$(for phase in $phases; do echo "- $phase"; done)

Обʼєкт може зʼявитися на будь-якому етапі. Ми підтримуємо всі одразу.
EOL

echo "✅ Фрактальний життєвий цикл активовано. Усі фази доступні 🔃"